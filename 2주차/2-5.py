name = input("이름을 입력하시오:") #변수 name에 이름을 입력받는다.
print(name, "씨 안녕하세요?") #name과 문자열을 출력한다. 
print("파이썬에 오신 것을 환영합니다.") #문자열을 출력한다.
num1=int(input("첫 번째 정수를 입력하시오:")) #변수 num1에 첫 번째 정수를 입력받는다.
num2=input(input("두 번째 정수를 입력하시오:")) #변수 num2에 두 번째 정수를 입력받는다.
sum=num1+num2 #num1과 num2의 값을 합친다.
print(num1,"과",num2,"의 합은",sum,"입니다.") #변수 num1과 num2, sum의 값과 문자열을 출력한다.
